                    <P class="text-justify">
                        Paduan suara atau kor (dari bahasa Belanda, koor) merupakan istilah yang merujuk kepada ensembel musik yang terdiri atas penyanyi-penyanyi maupun musik yang dibawakan oleh ensembel tersebut. Umumnya suatu kelompok paduan suara membawakan musik paduan suara yang terdiri atas beberapa bagian suara. Paduan suara biasanya dipimpin oleh seorang dirigen atau choirmaster yang umumnya sekaligus adalah pelatih paduan suara tersebut. Umumnya paduan suara terdiri atas empat bagian suara (misalnya sopran, alto, tenor, dan bas), walaupun dapat dikatakan bahwa tidak ada batasan jumlah suara yang terdapat dalam paduan suara. Selain empat suara, jumlah jenis suara yang paling lazim dalam paduan suara adalah tiga, lima, enam, dan delapan. Bila menyanyi dengan satu suara, paduan suara tersebut diistilahkan menyanyi secara unisono.
